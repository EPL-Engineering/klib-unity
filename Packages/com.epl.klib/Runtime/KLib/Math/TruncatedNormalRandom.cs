        /// <summary>
        /// Generate one random number from a truncated normal probability distribution.
        /// </summary>
        /// <param name="a">Minimum value of random number.</param>
        /// <param name="b">Maximum value of random number</param>
        /// <param name="mu">Mean of underlying normal distribution.</param>
        /// <param name="sigma">Standard deviation of underlying normal distribution.</param>
        /// <returns>Random number</returns>
        public float Next(float a, float b, float mu, float sigma)
        {
            if (mu != 0 || sigma != 1)
            {
                a = (a - mu) / sigma;
                b = (b - mu) / sigma;
            }

            float rn = rtstdnorm(a, b);
            rn = rn * sigma + mu;
            return rn;
        }

        /// <summary>
        /// RTSTDNORM Pseudorandom numbers from a truncated (normalized) Gaussian distribution (i.e. rtnorm(a,b,0,1)).
        /// </summary>
        /// <param name="a"></param>
        /// <param name="b"></param>
        /// <returns></returns>
        private float rtstdnorm(float a, float b)
        {
            float r = 0;

            float z = 0;
            float e;
            float sim;

            int k = 0, ka, kb;

            // Left and right limits
            float xmin = -2.00443204036f;
            float xmax = 3.48672170399f;

            // Check if a < b
//            if (a >= b)
//                throw new ArgumentOutOfRangeException("B must be greater than A.");

            // Check if |a| < |b|
            if (Mathf.Abs(a) > Mathf.Abs(b))
            {
                r = -rtstdnorm(-b, -a);
            }
            // If a in the right tail (a > xmax), use rejection algorithm with
            // a truncated exponential proposal
            else if (a > xmax)
            {
                float twoasq = 2 * a * a;
                float expab = Mathf.Exp(-a * (b - a)) - 1;

                bool stop = false;
                while (!stop)
                {
//                    z = Mathf.Log(1 + Random.value * expab);
//                    e = -Mathf.Log(Random.value);
                    z = Mathf.Log(1 + (float)_randu.NextDouble() * expab);
                    e = -Mathf.Log((float)_randu.NextDouble());
                    stop = (twoasq * e > z * z);
                }
                r = a - z / a;
            }

            // If a in the left tail (a < xmin), use rejection algorithm with
            // a Gaussian proposal
            else if (a < xmin)
            {
                bool stop = false;
                while (!stop)
                {
                    r = _randg.Next();
                    stop = (r >= a) && (r <= b);
                }
            }

            // In other cases (xmin < a < xmax), use Chopin's algorithm
            else
            {
                // Design variables
                float kmin = 5;                       // if kb-ka < kmin then use a rejection algorithm
                float INVH = 1631.73284006f;           // 1/h, h being the minimal interval range
                int I0 = 3271;                      // = - floor(x(1)/h)
                float ALPHA = 1.837877066409345f;      // = Mathf.Log(2*pi)
                int N = 4000;                          // Index of the right tail
                float yl0 = 0.053513975472f;           // y_l of the leftmost rectangle
                float ylN = 0.000914116389555f;        // y_l of the rightmost rectangle

                // Compute ka and kb
                int i = I0 + (int)Mathf.Floor(a * INVH);
                ka = ncell[i];                // +1 due to index offset in Matlab
                if (b >= xmax)
                {
                    kb = N;
                }
                else
                {
                    i = I0 + (int)Mathf.Floor(b * INVH);
                    kb = ncell[i];            // +1 due to index offset in Matlab
                }

                // If |b-a| is small, use rejection algorithm with a truncated exponential proposal
                if (Mathf.Abs(kb - ka) < kmin)
                {
                    bool stop = false;
                    float twoasq = 2 * a * a;
                    float expab = Mathf.Exp(-a * (b - a)) - 1;
                    while (!stop)
                    {
                        z = Mathf.Log(1 + (float)_randu.NextDouble() * expab);
                        e = -Mathf.Log((float)_randu.NextDouble());
                        stop = (twoasq * e > z * z);
                    }
                    r = a - z / a;
                    return r;
                }

                while (true)
                {
                    // Sample integer between ka and kb
					k = _randu.Next (ka, kb);
                    if (k == N)
                    {
                        // Right tail

                        float lbound = x[x.Length - 1];
                        z = -Mathf.Log((float)_randu.NextDouble());
                        e = -Mathf.Log((float)_randu.NextDouble());
                        z = z / lbound;
                        if ((z * z <= 2 * e) && (z < b - lbound))
                        {
                            // Accept this proposition, otherwise reject
                            r = lbound + z;
                            return r;
                        }
                    }
                    else if ((k <= ka + 2) || (k >= kb && b < xmax))
                    {
                        // Two leftmost and rightmost regions

                        sim = x[k] + (x[k + 1] - x[k]) * (float)_randu.NextDouble();
                        if ((sim >= a) && (sim <= b))
                        {
                            // Accept this proposition, otherwise reject
                            float simy = yu[k] * (float)_randu.NextDouble();

                            float ylk;

                            // Compute y_l from y_k
                            if (k == 1)
                            {
                                ylk = yl0;
                            }
                            else if (k == N)
                            {
                                ylk = ylN;
                            }
                            else if (k <= 1954)
                            {
                                ylk = yu[k];
                                //ylk = yu[k - 1];
                            }
                            else
                            {
                                ylk = yu[k + 1];
                            }

                            if ((simy < ylk) || (sim * sim + 2 * Mathf.Log(simy) + ALPHA < 0))
                            {
                                r = sim;
                                return r;
                            }
                        }
                    }

                    else
                    {

                        // All the other boxes

                        float u = (float)_randu.NextDouble();
                        float simy = yu[k] * u;
                        float d = x[k + 1] - x[k];

                        float ylk;
                        // Compute y_l from y_k
                        if (k == 0)
                        {
                            ylk = yl0;
                        }
                        else if (k == N - 1)
                        {
                            ylk = ylN;
                        }
                        else if (k <= 1953)
                        {
                            ylk = yu[k - 1];
                        }
                        else
                        {
                            ylk = yu[k + 1];
                        }

                        if (simy < ylk)
                        {  // That's what happens most of the time 
                            r = x[k] + u * d * yu[k] / ylk;
                            return r;
                        }
                        sim = x[k] + d * (float)_randu.NextDouble();
                        // Otherwise, check you're below the pdf curve
                        if (sim * sim + 2 * Mathf.Log(simy) + ALPHA < 0)
                        {
                            r = sim;
                            return r;
                        }
                    }
                }
            }

            return r;
        }


