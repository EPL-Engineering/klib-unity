        public static float[] PDF(float dx, out float[] x, float a, float b)
		{
			return PDF (dx, out x, a, b, 0, 1);
		}
		
        /// <summary>
        /// 
        /// </summary>
        /// <param name="dx"></param>
        /// <param name="x"></param>
        /// <param name="a"></param>
        /// <param name="b"></param>
        /// <param name="mu"></param>
        /// <param name="sigma"></param>
        /// <returns></returns>
        public static float[] PDF(float dx, out float[] x, float a, float b, float mu, float sigma)
        {
            float xmin = (a < 0) ? 1.5f * a : 0.5f * a;
            float xmax = (b < 0) ? 0.5f * b : 1.5f * b;

            int n = (int)Mathf.Round((xmax - xmin) / dx) + 1;
            x = new float[n];
            x[0] = xmin;
            for (int k = 1; k < n; k++)
            {
                x[k] = x[k - 1] + dx;
            }

            return PDF(x, a, b, mu, sigma);

        }

