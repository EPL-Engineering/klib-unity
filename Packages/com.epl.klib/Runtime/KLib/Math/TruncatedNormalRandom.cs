        /// <summary>
        /// 
        /// </summary>
        /// <param name="x"></param>
        /// <param name="a"></param>
        /// <param name="b"></param>
        /// <param name="mu"></param>
        /// <param name="sigma"></param>
        /// <returns></returns>
        public static float[] PDF(float[] x, float a, float b, float mu, float sigma)
        {
            float[] pdf = new float[x.Length];

            //float yMax = Mathf.Exp(-Mathf.Pow((x[k] - mu), 2) / (2 * sigma * sigma));
            for (int k = 0; k < x.Length; k++)
            {
                if (x[k] > a && x[k] < b)
                {
                    pdf[k] = Mathf.Exp(-Mathf.Pow((x[k] - mu), 2) / (2 * sigma * sigma));
                }
            }

            return pdf;
        }

        public float Next(float a, float b)
		{
			return Next (a, b, 0, 1);
